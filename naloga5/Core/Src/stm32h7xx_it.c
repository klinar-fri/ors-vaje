// merimo št pritiskov, ker je izjema prožena za obe fronti se i povečuje za 2 vsak pritisk !!
volatile int i = 0;
/*
	i % 4 == 1: zadnja fronta prvega pritiska -> vklopi
	i % 4 == 2: prva fronta prvega pritiska -> ostane prižgana
	i % 4 == 3: zadnja fronta drugega pritiska -> ugasni led
	i % 4 == 0: prva fronta drugega pritiska -> ostane ugasnena
 */

// čas od zadnjega tick-a
volatile int32_t lastTick = 0;

// čas pritiska gumba
volatile int32_t pritiskStartTick = 0;

/* USER CODE BEGIN 1 */
void EXTI15_10_IRQHandler(){
	if (__HAL_GPIO_EXTI_GET_IT(GPIO_PIN_13)) {

		// ali je od zadnje prekinitve minilo več kot 50-100ms
		// če je i++, sicer zgolj clear_it

		volatile int32_t currentTick = HAL_GetTick();

		if((currentTick - lastTick) > 50){
			i++;
			if(i % 4 == 1){ //
				HAL_GPIO_WritePin(GPIOD, GPIO_PIN_3, ENABLE); // on 1
				HAL_GPIO_WritePin(GPIOJ, GPIO_PIN_2, DISABLE); // on 2
				pritiskStartTick = currentTick;
			}else if(i % 4 == 3){
				HAL_GPIO_WritePin(GPIOD, GPIO_PIN_3, DISABLE); // off 1
				HAL_GPIO_WritePin(GPIOJ, GPIO_PIN_2, ENABLE); // off 2
			}

			// spustimo gumb (prva fronta prvega pritiska)
				if(currentTick - pritiskStartTick >= 5000){
					HAL_GPIO_WritePin(GPIOI, GPIO_PIN_13, DISABLE); // on 3
				}
		}

		lastTick = currentTick;

	}
	__HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_13);
}
/* USER CODE END 1 */
